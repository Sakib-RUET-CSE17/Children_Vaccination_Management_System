    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
            if (jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals("") || jTextField5.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Field Must Not Be Empty!");

            } else {
                String sql = "select img_location from child where id = ?";
                pst = con.prepareStatement(sql);
                pst.setString(1, jTextField1.getText());
                rs = pst.executeQuery();
                if (rs.next()) {
                    String loc = rs.getString("img_location");
                    File f = new File(loc);
                    Desktop d = Desktop.getDesktop();
                    d.open(f);

                } else {
                    JOptionPane.showMessageDialog(null, "Image Not Found!");
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please insert Child ID");
        } else {
            try {
                String query = "select * from child where id = '" + jTextField1.getText() + "' ";
                pst = con.prepareStatement(query);
                rs = pst.executeQuery();
                if (rs.next()) {
                    jTextField1.setText(rs.getString("id"));
                    jTextField2.setText(rs.getString("Name"));
                    jTextField6.setText(rs.getString("Father"));
                    DOB = rs.getString("DOB");
                    jTextField3.setText(DOB);
                    calculateAge();
                    String Age = Integer.toString(resultYear) + " Year " + Integer.toString(resultMonth) + " Month " + Integer.toString(resultDay) + " Days";
                    jTextField4.setText(Age);
                    jTextField5.setText(rs.getString("BloodGroup"));
                    jTextField7.setText(rs.getString("Address"));
                    jTextField12.setText(rs.getString("Contact"));
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        //insert_vaccin();
        int k = 0;
        int j = 0;
        int due_dose = 0;
        for (int i = 0; i < vaccin_no; i++) {
            int age = resultYear * 365 + resultMonth * 12 + resultDay;
            for (j = 0; j < dose[i]; j++) {
                if (vaccin[i][j] >= age) {
                    k = vaccin[i][j] - age;
                    break;
                }
            }
            due_dose = dose[i] - j;
            jTable1.setValueAt(due_dose, i + 1, 3);
            if (due_dose > 0) {
                try {
                    addDays(k, i + 1);
                } catch (ParseException ex) {
                    Logger.getLogger(Vaccin_Date.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }


    }//GEN-LAST:event_jButton5ActionPerformed

