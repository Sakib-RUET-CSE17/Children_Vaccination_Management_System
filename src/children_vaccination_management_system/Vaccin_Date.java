    public void addDays(int k, int row) throws ParseException {
        DateFormat formatter = new SimpleDateFormat("dd-MM-yyyy");
        Calendar c = Calendar.getInstance();
        c.setTime(formatter.parse(DOB));
        c.add(Calendar.DAY_OF_MONTH, k);
        jTable1.setValueAt(formatter.format(c.getTimeInMillis()), row, 4);
        System.out.println(formatter.format(c.getTimeInMillis()));
    }

    public void calculateAge() throws ParseException {
        String dob[] = DOB.split("-");
        int year = Integer.parseInt(dob[2]);
        int month = Integer.parseInt(dob[1]);
        int day = Integer.parseInt(dob[0]);
        LocalDate selectedDate = LocalDate.of(year, month, day);
        LocalDate currentDate = LocalDate.now();

        //addDays();
        resultYear = Period.between(selectedDate, currentDate).getYears();
        resultMonth = Period.between(selectedDate, currentDate).getMonths();
        resultDay = Period.between(selectedDate, currentDate).getDays();
    }

